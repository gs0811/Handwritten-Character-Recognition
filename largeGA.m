                                ft = [ft exp(-lambda(k,j)*sqrt((px(k,j)-gfeatureSetx(m,i))^2+(py(k,j)-gfeatureSety(m,i))^2))];
                            end
                        end
                        gfeatureSet = cat(1,gfeatureSet,ft);
                    end
                    
                    word=[];
                    for m=271:315
                        distance=[];
                        for i=1:size(letter,2)-45
